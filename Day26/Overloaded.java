public class Overloaded {

    public double sumOf(double v1, double v2) {
          return v1 + v2;
    }

    public double sumOf(double v1, double v2, double v3) {
          return v1 + v2 + v3;
    }

    public double sumOf(double v1, double v2, double v3, double v4) {
          return v1 + v2 + v3 + v4;
    }
}
